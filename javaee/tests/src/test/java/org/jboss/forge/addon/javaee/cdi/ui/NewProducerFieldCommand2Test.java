@RunWith(Arquillian.class)
public class NewProducerFieldCommand2Test
{
   @Deployment
   @Dependencies({
            @AddonDependency(name = "org.jboss.forge.addon:ui"),
            @AddonDependency(name = "org.jboss.forge.addon:ui-test-harness"),
            @AddonDependency(name = "org.jboss.forge.addon:javaee"),
            @AddonDependency(name = "org.jboss.forge.addon:maven")
   })
   public static ForgeArchive getDeployment()
   {
      return ShrinkWrap
               .create(ForgeArchive.class)
               .addClass(ProjectHelper.class)
               .addBeansXML()
               .addAsAddonDependencies(
                        AddonDependencyEntry.create("org.jboss.forge.furnace.container:cdi"),
                        AddonDependencyEntry.create("org.jboss.forge.addon:projects"),
                        AddonDependencyEntry.create("org.jboss.forge.addon:javaee"),
                        AddonDependencyEntry.create("org.jboss.forge.addon:maven"),
                        AddonDependencyEntry.create("org.jboss.forge.addon:ui"),
                        AddonDependencyEntry.create("org.jboss.forge.addon:ui-test-harness")
               );
   }

   @Inject
   private UITestHarness testHarness;

   @Inject
   private ProjectHelper projectHelper;

   private JavaClassSource targetClass;

   private Project project;

   @Test
   public void testCreateNewInterceptor() throws Exception
   {
      Project project = projectHelper.createJavaLibraryProject();
      projectHelper.installCDI_1_0(project);
      CommandController controller = testHarness.createCommandController(NewProducerFieldCommand.class,
               project.getRoot());
      controller.initialize();
      createTargetClass();
      String fieldName = "firstName";
      controller.setValueFor("named", fieldName);
      controller.setValueFor("type", "java.lang.String");
      Assert.assertTrue(controller.isValid());
      Assert.assertTrue(controller.canExecute());
      Result result = controller.execute();
      Assert.assertThat(result, is(not(instanceOf(Failed.class))));

      assertNotNull(targetClass.getField(fieldName));
      assertTrue(targetClass.getField(fieldName).isPrivate());
      assertTrue(targetClass.getField(fieldName).hasAnnotation(Produces.class));
      assertNull(targetClass.getMethod("getFirstName"));

   }

   private void createTargetClass() throws FileNotFoundException
   {
      targetClass = Roaster.parse(JavaClassSource.class, "public class Test{}");
      project.getFacet(JavaSourceFacet.class).saveJavaSource(targetClass);
   }

}
